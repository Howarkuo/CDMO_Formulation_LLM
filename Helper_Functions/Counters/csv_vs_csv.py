# --- HELPER FUNCTIONS ---

def normalize_doi(doi_string):
    """
    Cleans a DOI string: 
    - Removes whitespace
    - Converts to lowercase
    - Removes common prefixes
    """
    clean = doi_string.strip().lower()
    clean = clean.replace("https://doi.org/", "").replace("http://dx.doi.org/", "")
    return clean

def extract_doi_map(raw_text, remove_prefix=None):
    """
    Parses text and extracts DOIs.
    Optionally removes a prefix (like 'MISSING:') before storing the line.
    """
    data_map = {}
    lines = raw_text.strip().split('\n')
    
    # Regex to find a standard DOI pattern (starts with 10.xxxx/)
    doi_pattern = re.compile(r"(10\.\d{4,9}/[-._;()/:a-zA-Z0-9]+)")
    
    for line in lines:
        if not line.strip(): continue
        
        # 1. CLEAN THE LINE
        clean_line = line.strip()
        if remove_prefix and clean_line.startswith(remove_prefix):
            clean_line = clean_line.replace(remove_prefix, "").strip()
            # Also remove specific trailing/leading commas if they were left behind
            if clean_line.startswith(","): clean_line = clean_line[1:].strip()

        # 2. FIND DOI
        match = doi_pattern.search(clean_line)
        if match:
            raw_doi = match.group(1)
            clean_doi = normalize_doi(raw_doi)
            
            # Store the CLEANED line (without "MISSING:")
            data_map[clean_doi] = clean_line
            
    return data_map

