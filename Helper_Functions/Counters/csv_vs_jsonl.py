    # Regex explanation:
    # "doi"   -> literal string "doi"
    # \s*:\s* -> colon with optional whitespace
    # "       -> opening quote
    # ([^"]+) -> CAPTURE everything that is NOT a quote (the DOI)
    # "       -> closing quote
    pattern = re.compile(r'"doi"\s*:\s*"([^"]+)"')
    
    matches = pattern.findall(raw_text)
    
    for m in matches:
        normalized = clean_doi(m)
        dois.add(normalized)
                
    return dois

