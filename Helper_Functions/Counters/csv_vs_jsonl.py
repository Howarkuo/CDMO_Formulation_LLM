def clean_doi(doi_string):
    """
    Standardizes DOIs:
    1. Lowercase.
    2. Strips whitespace.
    3. Removes common prefixes and trailing punctuation.
    """
    if not doi_string: return None
    s = doi_string.strip()
    # Remove common URL prefixes
    s = s.replace("https://doi.org/", "").replace("http://dx.doi.org/", "")
    # Remove surrounding quotes if regex captured them by mistake
    s = s.replace('"', '').replace("'", "")
    # Strip trailing punctuation often found in CSVs (periods, commas)
    s = s.strip(".,;()")
    return s.lower()

def extract_csv_dois(raw_text):
    """
    Extracts DOIs from CSV text using Regex for '10.xxxx/...'
    """
    data_map = {}
    lines = raw_text.strip().split('\n')
    
    # Regex for a DOI starting with 10.
    # It stops at the first whitespace to avoid grabbing extra text
    doi_pattern = re.compile(r"(10\.\d{4,9}/\S+)")
    
    for line in lines:
        if not line.strip(): continue
        
        # Remove "MISSING:" prefix if present
        clean_line = line.replace("MISSING:", "").strip()
        
        match = doi_pattern.search(clean_line)
        if match:
            raw_doi = match.group(1)
            normalized = clean_doi(raw_doi)
            data_map[normalized] = clean_line
            
    return data_map

