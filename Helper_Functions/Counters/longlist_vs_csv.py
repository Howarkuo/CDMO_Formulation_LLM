def parse_bigger_list(raw_text):
    """
    Parses CSV format: PMID, PMCID, Publisher, DOI
    Returns a dictionary: { 'PMCxxxx': 'Original Line String' }
    """
    data_map = {}
    lines = raw_text.strip().split('\n')
    for line in lines:
        if not line.strip(): continue
        parts = line.split(',')
        if len(parts) >= 2:
            # PMCID is the 2nd item (index 1)
            pmcid = parts[1].strip()
            data_map[pmcid] = line.strip()
    return data_map

def parse_smaller_list(raw_text):
    """
    Parses Log format using Regex to find 'PMC' followed by digits.
    Returns a set of found PMCIDs.
    """
    # Regex looks for "PMC" followed immediately by 1 or more digits
    return set(re.findall(r"(PMC\d+)", raw_text))

